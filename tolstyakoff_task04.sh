# ============================================================
# 2. Удобные ls'ы
# ============================================================
alias l='ls -A --color=always -v --group-directories-first'
alias ll='ls -Al --color=always -v --group-directories-first --file-type --human-readable --sort=time'

# ============================================================
# 3. cd + ls
# ============================================================
cd() { builtin cd "$@" && l; }

# ============================================================
# 4. cd + fzf (cdf)
# ============================================================
alias cdf='cd "$(dirname "$(fzf)")"'

