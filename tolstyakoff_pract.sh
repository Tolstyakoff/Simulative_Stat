#!/bin/bash
# ============================================================
# Контрольная работа по модулю Linux
# Скрипт выполняет все задания по порядку
# ============================================================

set -e  # прерывать выполнение при любой ошибке

# ------------------------------------------------------------
# 1. Переменная путей
# ------------------------------------------------------------
# Путь к текущему файлу скрипта (полный)
SCRIPT_PATH="$(realpath "$0")"
echo "Путь к текущему скрипту: $SCRIPT_PATH"

# Имя файла лога (создадим в текущей директории)
LOG_FILE="$PWD/script.log"
# Переменная с именем файла (последний элемент) – используем basename
LOG_BASENAME="$(basename "$LOG_FILE")"
echo "Имя файла лога: $LOG_BASENAME"

# ------------------------------------------------------------
# 2. Вывод операционной системы
# ------------------------------------------------------------
echo "=== Информация об ОС ==="
# Полная информация (обычно в /etc/os-release или команда hostnamectl)
if command -v lsb_release &> /dev/null; then
    lsb_release -a 2>/dev/null || true
else
    cat /etc/os-release 2>/dev/null || true
fi

echo "=== Строки, содержащие UBUNTU (без учёта регистра) ==="
# Ищем в /etc/os-release, можно также в выводе lsb_release
grep -i "ubuntu" /etc/os-release 2>/dev/null || echo "(не найдено)"

# ------------------------------------------------------------
# 3. Обновление системы
# ------------------------------------------------------------
if [ "$EUID" -eq 0 ]; then
    echo "Запуск от root: обновляем систему..."
    apt update && apt upgrade -y
else
    echo "Скрипт выполняется НЕ от root. Обновление пропущено."
fi

# ------------------------------------------------------------
# 4. Временный alias через shopt -s expand_aliases
# ------------------------------------------------------------
shopt -s expand_aliases
alias qq='echo "Временный алиас отработал!"'
qq

# ------------------------------------------------------------
# 5. Создание файла лога с перезаписыванием
# ------------------------------------------------------------
if [ -f "$LOG_FILE" ]; then
    rm "$LOG_FILE"
fi
touch "$LOG_FILE"
echo "Файл лога создан: $LOG_FILE"

# ------------------------------------------------------------
# 6. Вывод списка файлов в текущей директории (добавляем в лог)
# ------------------------------------------------------------
ls -la >> "$LOG_FILE"

# ------------------------------------------------------------
# 7. Посчитать количество строк в логе и вывести на экран
# ------------------------------------------------------------
LINE_COUNT="$(wc -l < "$LOG_FILE")"
echo "Количество строк в лог-файле: $LINE_COUNT"

# ------------------------------------------------------------
# 8. Использование переменных окружения
# ------------------------------------------------------------
echo "Путь к лог-файлу (переменная LOG_FILE): $LOG_FILE"
echo "Домашняя директория (HOME): $HOME"

# ------------------------------------------------------------
# 9. Функция (простой пример – выводит дату)
# ------------------------------------------------------------
show_date() {
    echo "Текущая дата и время: $(date)"
}
show_date

# ------------------------------------------------------------
# 10. Вызов скрипта с аргументом --info
# ------------------------------------------------------------
if [ "$1" = "--info" ]; then
    show_date
else
    echo "Запустите скрипт с аргументом --info"
fi

# ------------------------------------------------------------
# 11. Права на файл (текущие, затем 777, затем снова)
# ------------------------------------------------------------
if [ -f "$LOG_FILE" ]; then
    echo "Текущие права на $LOG_FILE:"
    ls -l "$LOG_FILE" | awk '{print $1}'
    chmod 777 "$LOG_FILE"
    echo "Права после chmod 777:"
    ls -l "$LOG_FILE" | awk '{print $1}'
else
    echo "Файл лога не найден"
fi

# ------------------------------------------------------------
# 12. ТОП-3 процессов по использованию памяти (записать в лог)
# ------------------------------------------------------------
echo "=== ТОП-3 процессов по памяти ===" >> "$LOG_FILE"
ps aux --sort=-%mem | head -n 4 >> "$LOG_FILE"

# ------------------------------------------------------------
# 13. Простейший curl (случайная шутка из API)
# ------------------------------------------------------------
echo "Случайная шутка (curl):"
curl -s https://official-joke-api.appspot.com/random_joke | python3 -c "import sys, json; d=json.load(sys.stdin); print(f'{d[\"setup\"]} - {d[\"punchline\"]}')" 2>/dev/null || echo "Ошибка: curl или python3 не установлены"
