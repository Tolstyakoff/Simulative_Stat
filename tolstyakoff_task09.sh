1. Решатель кроссвордов
В Ubuntu по умолчанию установлен пакет words, который позволяет использовать англоязычный словарь для своих нужд. Этот список всех английских слов находится в файле /usr/share/dict/words.

Напишите маленький скрипт wordhint, который будет выводить список подходящих для решения кроссворда слов.

Например:

Bash
1
bash wordhint .a..ch
Выведет:

1
2
3
4
5
Banach
Baruch
haunch
launch
paunch
К сожалению, по умолчанию не доступна русскоязычная версия сего чуда техники.



2. Электронные почты
Написать команду, которая в файле emails.txt найдет все строки с названиями электронных почт.

Пусть наши электронные почты будут иметь следующий формат:

Имя (до @): имеет размер от 6 символов, содержит любые латинские символы и цифры.
Доменное имя (после @, до .): размер от 3 до 12, латинские символы в нижнем регистре.
Доменная зона (после .): от 3 до 6 латинских символов в нижнем регистре.
Пример электронной почты: someone2000@mail.com.



