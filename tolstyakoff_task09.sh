6. Логирование конвейера
Реализуйте скрипт-обертку teepipes, которая при помощи команды tee будет выполнять логирование в файл teepipes.log на каждом этапе конвейера исходной команды (то есть будет сохранять все промежуточные результаты). Исходную команду нужно передавать обернутой в одинарные кавычки:

Bash
1
bash teepipes 'do_a | do_b | do_c'
Вам также понадобится команда eval.

Она позволяет интерпретировать указанную в аргументе строку как отдельную команду:

Bash
1
2
eval 'echo Hello World!'
# Hello World!
Для проверки работоспособности можете попробовать использовать скрипт из практики 7, задания 6.2 :)

И не забудьте про экранирование кавычек в исходной команде!



7. Занятые порты
У вас есть директория, в которой лежат все ваши рабочие сервисы приложений (она может называется apps/ или projects/).

Внутри проектов приложений есть конфигурационные и не только файлы, в которых содержатся настройки и информация используемых портов для соответствующего сервиса.

Вот пример указанных таким образом настроек портов в файлах конфигураций различных проектов:

