8. Последние изменения
Напишите скрипт recentpys. Он выведет все Python-файлы (с расширением .py), которые были модифицированы за последний месяц во всех вложенных директориях, начиная с текущей. Вывод должен быть отсортирован по дате модификации от недавнего до давнего. В выводе, помимо пути до самих файлов, должна быть рядом указана дата их модификации

У команды find огромное количество опций и многие из них, как я уже упоминал, можно воспроизвести комбинируя find с другими утилитами. Если не можете найти подходящий флаг, то не усложняйте себе жизнь - используйте известную и понятную вам альтернативу. То же самое работает и наоборот: если есть желание, то можете почти все операции попробовать проделать одной хитрой комбинацией флагов.



