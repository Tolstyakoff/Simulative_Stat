Ваша задача написать скрипт busyports, который при запуске будет выводить список всех портов, настроенных для использования всех сервисов и приложений, расположенных в текущей директории (они могут находиться в любых поддиректориях).



Также напротив используемого порта необходимо вывести путь до файла, в котором расположена данная настройка.

Например, следующим образом:

